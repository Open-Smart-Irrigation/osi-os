#!/usr/bin/env node
'use strict';

const assert = require('node:assert/strict');
const crypto = require('node:crypto');
const fs = require('node:fs');
const os = require('node:os');
const path = require('node:path');

const repoRoot = path.resolve(__dirname, '..');
const core = require('./journal-catalog-core');
const source = require('../docs/superpowers/specs/agroscope-open-field/catalog.json');
const generator = require('./generate-journal-catalog');

function stableStringify(value) {
  if (Array.isArray(value)) return '[' + value.map(stableStringify).join(',') + ']';
  if (value && typeof value === 'object') {
    return '{' + Object.keys(value).sort().map((key) =>
      JSON.stringify(key) + ':' + stableStringify(value[key])
    ).join(',') + '}';
  }
  return JSON.stringify(value);
}

function sha256(value) {
  return crypto.createHash('sha256').update(value).digest('hex');
}

for (const exportName of [
  'compileCatalog',
  'validateCore',
  'validateSource',
  'replaceSeedBlock',
  'expectedManifestText',
  'writeGeneratedArtifacts',
]) {
  assert.equal(
    typeof generator[exportName],
    'function',
    `generator must export ${exportName} as a testable compiler/validation seam`
  );
}
assert.ok(
  Array.isArray(generator.CATALOG_MIGRATIONS) && generator.CATALOG_MIGRATIONS.length >= 2,
  'generator must export the CATALOG_MIGRATIONS registry so a v-next migration can be registered without editing frozen files'
);

const migrationsDir = path.join(repoRoot, 'database/migrations/ordered');
const v1Path = path.join(migrationsDir, '0019__journal_catalog_v1.sql');
const v2Path = path.join(migrationsDir, '0022__journal_catalog_v2.sql');
const seedPath = path.join(repoRoot, 'database/seed-blank.sql');
const manifestPath = path.join(migrationsDir, 'CHECKSUMS.json');

// Only files that must already exist and stay untouched by pure compilation
// are snapshotted here. 0022 may or may not exist yet on disk depending on
// whether the generator has been run to write it in this worktree; its
// existence must not flip merely from calling compileCatalog.
const stableArtifactPaths = [v1Path, seedPath, manifestPath];
const beforeCompile = stableArtifactPaths.map((artifactPath) => ({
  content: fs.readFileSync(artifactPath),
  mtimeMs: fs.statSync(artifactPath).mtimeMs,
}));
const v2ExistedBeforeCompile = fs.existsSync(v2Path);

const compiled = generator.compileCatalog(core, source);
const compiledAgain = generator.compileCatalog(core, source);
assert.deepEqual(
  compiled.migrations.map((migration) => migration.content),
  compiledAgain.migrations.map((migration) => migration.content),
  'pure compilation must be deterministic',
);
assert.equal(compiled.seedBlock, compiledAgain.seedBlock, 'seed compilation must be deterministic');
assert.deepEqual(
  stableArtifactPaths.map((artifactPath) => ({
    content: fs.readFileSync(artifactPath),
    mtimeMs: fs.statSync(artifactPath).mtimeMs,
  })),
  beforeCompile,
  'pure compilation must not read-modify-write repository artifacts',
);
assert.equal(
  fs.existsSync(v2Path),
  v2ExistedBeforeCompile,
  'pure compilation must not create the not-yet-written v2 delta migration file',
);

// --- Versioned delta publishing (Task 27 / Deliverable A) -----------------

assert.deepEqual(
  compiled.migrations.map((migration) => ({ version: migration.version, name: migration.name })),
  [
    { version: 1, name: '0019__journal_catalog_v1.sql' },
    { version: 2, name: '0022__journal_catalog_v2.sql' },
    { version: 3, name: '0023__journal_catalog_v3.sql' },
  ],
  'compileCatalog must emit exactly the registered catalog migrations, in version order',
);

assert.equal(
  compiled.migrations[0].content,
  fs.readFileSync(v1Path, 'utf8'),
  '0019 must stay byte-identical now that farmer_quick@2 exists in core',
);

const v2Content = compiled.migrations[1].content;
assert.match(v2Content, /^-- risk: data$/m, '0022 must declare a data-risk header');
assert.match(
  v2Content,
  /GENERATED by scripts\/generate-journal-catalog\.js/,
  '0022 must carry the generated-file header',
);
assert.ok(
  !v2Content.includes('-- journal_vocab\n'),
  '0022 must not touch journal_vocab: nothing new since v1 there',
);
assert.ok(
  !v2Content.includes('-- journal_vocab_mappings\n'),
  '0022 must not touch journal_vocab_mappings: nothing new since v1 there',
);
assert.ok(
  !v2Content.includes('-- journal_layouts\n'),
  '0022 must not touch journal_layouts: nothing new since v1 there',
);
assert.ok(
  !v2Content.includes('-- journal_products\n'),
  '0022 must not touch journal_products: nothing new since v1 there',
);
assert.ok(
  v2Content.includes('-- journal_templates\n'),
  '0022 must contain the journal_templates delta section',
);
assert.equal(
  (v2Content.match(/INSERT INTO journal_templates\(/g) || []).length,
  1,
  '0022 must insert exactly one new row: the farmer_quick version-2 template',
);
assert.match(
  v2Content,
  /SELECT 'farmer_quick',2,/,
  '0022 must insert the farmer_quick version-2 row',
);
assert.match(v2Content, /catalog_version=2/, '0022 must bump journal_catalog_state to version 2');

const hashInput = compiled.rows.map((row) => ({
  table: row.table,
  key: row.key,
  columns: row.columns,
  values: row.values,
}));
assert.equal(
  compiled.catalogHash,
  sha256(stableStringify(hashInput)),
  'catalog hash must be independently reproducible from generated row content',
);
const mappingRows = compiled.rows.filter((row) => row.table === 'journal_vocab_mappings');
assert.equal(mappingRows.length, 7, 'compiled row content must include seven standard mappings');

// --- v-next extensibility: a hypothetical v4 must be a pure delta ---------
// (v3 is now real — farmer_quick@3 / the layout v3 rows, Slice BC — so the
// extensibility probe moves to a hypothetical v4 on top of it.)

const v3Definition = core.templates.find(
  (template) => template.code === 'farmer_quick' && template.version === 3,
).definition;
const coreWithV4 = {
  ...core,
  templates: [
    ...core.templates,
    {
      code: 'farmer_quick',
      version: 4,
      label: 'Quick',
      definition: { ...v3Definition, max_primary_fields: 6 },
    },
  ],
};
const registryWithV4 = [...generator.CATALOG_MIGRATIONS, { version: 4, name: 'test-only-v4.sql' }];
const compiledWithV4 = generator.compileCatalog(coreWithV4, source, registryWithV4);
assert.equal(
  compiledWithV4.migrations.length,
  4,
  'adding a v-next row must add exactly one new delta migration',
);
assert.equal(
  compiledWithV4.migrations[0].content,
  compiled.migrations[0].content,
  'adding a v-next row must leave the v1 migration byte-identical',
);
assert.equal(
  compiledWithV4.migrations[1].content,
  compiled.migrations[1].content,
  'adding a v-next row must leave the v2 migration byte-identical',
);
assert.equal(
  compiledWithV4.migrations[2].content,
  compiled.migrations[2].content,
  'adding a v-next row must leave the v3 migration byte-identical',
);
assert.equal(compiledWithV4.migrations[3].name, 'test-only-v4.sql');
assert.equal(
  (compiledWithV4.migrations[3].content.match(/INSERT INTO journal_templates\(/g) || []).length,
  1,
  'the v4 delta must contain exactly its own new row, nothing carried over from v1/v2/v3',
);

assert.throws(
  () => generator.compileCatalog(coreWithV4, source, generator.CATALOG_MIGRATIONS),
  /no CATALOG_MIGRATIONS entry|catalog version 4/i,
  'compileCatalog must refuse to silently drop a catalog version with no registered migration file',
);

// --- Slice BC / Task BC1: farmer_quick@3 quick_fields completeness -------

const quickTemplateV3 = core.templates.find(
  (template) => template.code === 'farmer_quick' && template.version === 3,
);
assert.ok(quickTemplateV3, 'core must define farmer_quick@3');
const quickFields = quickTemplateV3.definition.quick_fields;
assert.ok(quickFields && typeof quickFields === 'object' && !Array.isArray(quickFields),
  'farmer_quick@3 must declare a quick_fields object');
const coreActivityCodes = core.activities.map((activity) => activity.code);
assert.equal(coreActivityCodes.length, 16, 'core must define exactly 16 activities');
assert.deepEqual(
  Object.keys(quickFields).sort(),
  [...coreActivityCodes].sort(),
  'farmer_quick@3 quick_fields must cover exactly every core activity, no more, no less',
);
const knownAttributeCodes = new Set(core.attributes.map((attribute) => attribute.code));
for (const [activityCode, fields] of Object.entries(quickFields)) {
  assert.ok(Array.isArray(fields) && fields.length > 0,
    `quick_fields.${activityCode} must be a nonempty array`);
  for (const field of fields) {
    assert.ok(
      field === 'note' || knownAttributeCodes.has(field),
      `quick_fields.${activityCode} references unknown field ${field}`,
    );
  }
}
// Negative test: an unmapped activity or an unknown field code must be
// rejected by compileCatalog's validation (validateQuickFieldsAndReadings),
// not silently accepted.
const coreWithIncompleteQuickFields = {
  ...core,
  templates: core.templates.map((template) => template.code === 'farmer_quick' && template.version === 3
    ? { ...template, definition: { ...template.definition, quick_fields: { irrigation: ['note'] } } }
    : template),
};
assert.throws(
  () => generator.compileCatalog(coreWithIncompleteQuickFields, source),
  /quick_fields must cover exactly every core activity/,
  'compileCatalog must reject quick_fields that do not cover every activity',
);
const coreWithBadQuickFieldCode = {
  ...core,
  templates: core.templates.map((template) => template.code === 'farmer_quick' && template.version === 3
    ? {
      ...template,
      definition: {
        ...template.definition,
        quick_fields: { ...quickFields, irrigation: ['attr.does_not_exist'] },
      },
    }
    : template),
};
assert.throws(
  () => generator.compileCatalog(coreWithBadQuickFieldCode, source),
  /references unknown field/,
  'compileCatalog must reject a quick_fields entry referencing an unknown field',
);

// --- Slice BC / Task BC3: layout v3 static/reading field split -----------

const v3Layouts = core.layouts.filter((layout) => layout.version === 3);
assert.deepEqual(
  v3Layouts.map((layout) => layout.code).sort(),
  ['greenhouse', 'lysimeter', 'open_field'],
  'core must publish a v3 row for every generic layout',
);
for (const layout of v3Layouts) {
  const readingFields = layout.definition.reading_fields || [];
  const staticFields = layout.definition.static_context_fields || [];
  const minimumFields = layout.definition.minimum_fields || [];
  assert.deepEqual(
    readingFields.filter((field) => minimumFields.includes(field)),
    [],
    `${layout.code}@3 must not retain a reading field in minimum_fields`,
  );
  assert.deepEqual(
    staticFields.filter((field) => !minimumFields.includes(field)),
    [],
    `${layout.code}@3 static_context_fields must be a subset of minimum_fields`,
  );
}
const lysimeterV3 = v3Layouts.find((layout) => layout.code === 'lysimeter');
assert.deepEqual(
  lysimeterV3.definition.reading_fields.slice().sort(),
  [
    'attr.drainage_volume', 'attr.interval_minutes', 'attr.mass_end', 'attr.mass_method',
    'attr.mass_start', 'attr.rain_input', 'attr.tare_mass', 'attr.water_input',
  ].sort(),
  'lysimeter@3 must move all 8 water-balance reading fields out of minimum_fields',
);
const openFieldV1 = core.layouts.find((layout) => layout.code === 'open_field' && layout.version === 1);
const openFieldV3 = core.layouts.find((layout) => layout.code === 'open_field' && layout.version === 3);
assert.deepEqual(
  openFieldV1.definition.minimum_fields,
  openFieldV3.definition.minimum_fields,
  'open_field@1 minimum_fields bytes must stay untouched, and @3 must keep the identical set ' +
    '(full_record/research parity) even though @3 also exposes static_context_fields',
);
assert.ok(
  !openFieldV3.definition.static_context_fields.includes('attr.treated_area'),
  'open_field@3 static_context_fields must exclude attr.treated_area (activity-variable, not plot-static)',
);

const changedCore = {
  ...core,
  activities: core.activities.map((activity) => {
    if (activity.code !== 'irrigation') return activity;
    return {
      ...activity,
      mappings: activity.mappings.map((mapping) => ({
        ...mapping,
        external_id: `${mapping.external_id}_TEST_MUTATION`,
      })),
    };
  }),
};
const changedCompiled = generator.compileCatalog(changedCore, source);
assert.notEqual(
  changedCompiled.catalogHash,
  compiled.catalogHash,
  'changing a standard mapping must change the generated catalog hash',
);
assert.notEqual(
  changedCompiled.migrations[0].content,
  compiled.migrations[0].content,
  'changing a v1-scoped fact must change the v1 migration content',
);

const sourceCategoryOwners = new Map();
for (const activity of core.activities) {
  for (const category of activity.agroscope_categories || []) {
    assert.ok(!sourceCategoryOwners.has(category), `duplicate Agroscope category policy ${category}`);
    sourceCategoryOwners.set(category, activity.code);
  }
}
assert.deepEqual(
  [...sourceCategoryOwners.keys()].sort(),
  source.categories.map((category) => category.code).sort(),
  'all category mapping policy must live on core activity rows'
);
const sourceUnitLabels = new Set();
for (const unit of core.units) {
  for (const binding of unit.source_bindings || []) sourceUnitLabels.add(binding.label);
}
assert.deepEqual(
  [...sourceUnitLabels].sort(),
  [...source.all_units].sort(),
  'all Agroscope unit binding policy must live on semantic core unit rows'
);
const generatorSource = fs.readFileSync(path.join(__dirname, 'generate-journal-catalog.js'), 'utf8');
assert.match(
  generatorSource,
  /if \(require\.main === module\)/,
  'requiring the generator must not execute its CLI entry point'
);
for (const forbiddenPolicyName of [
  'CATEGORY_ACTIVITY',
  'UNIT_BINDINGS',
  'validateRepresentativeBindings',
]) {
  assert.ok(
    !generatorSource.includes(forbiddenPolicyName),
    `generator must not retain hard-coded policy ${forbiddenPolicyName}`
  );
}

const markerFreeSeed = '-- base seed\n';
const withOneBlock = generator.replaceSeedBlock(markerFreeSeed, compiled.seedBlock);
assert.equal(
  withOneBlock.split('-- BEGIN GENERATED JOURNAL CATALOG V1').length - 1,
  1,
  'marker-free seed gains exactly one begin marker'
);
assert.equal(
  withOneBlock.split('-- END GENERATED JOURNAL CATALOG V1').length - 1,
  1,
  'marker-free seed gains exactly one end marker'
);
assert.throws(
  () => generator.replaceSeedBlock(withOneBlock + '\n' + compiled.seedBlock, compiled.seedBlock),
  /duplicate|more than one/i,
  'duplicate complete generated seed blocks must be rejected'
);
assert.throws(
  () => generator.replaceSeedBlock(
    '-- BEGIN GENERATED JOURNAL CATALOG V1\nincomplete\n',
    compiled.seedBlock
  ),
  /incomplete|marker/i,
  'an incomplete generated seed block must be rejected'
);

const tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), 'journal-generator-'));
try {
  const tmpMigrationsDir = path.join(tmpDir, 'ordered');
  fs.mkdirSync(tmpMigrationsDir);
  const paths = {
    migrationsDir: tmpMigrationsDir,
    seedPath: path.join(tmpDir, 'seed-blank.sql'),
    manifestPath: path.join(tmpDir, 'CHECKSUMS.json'),
  };
  fs.writeFileSync(paths.seedPath, markerFreeSeed);
  fs.writeFileSync(paths.manifestPath, '{}\n');
  generator.writeGeneratedArtifacts(compiled, paths);
  const installedPath = (migration) => path.join(tmpMigrationsDir, migration.name);
  for (const migration of compiled.migrations) {
    assert.equal(fs.readFileSync(installedPath(migration), 'utf8'), migration.content);
  }
  assert.equal(
    fs.readFileSync(paths.seedPath, 'utf8'),
    generator.replaceSeedBlock(markerFreeSeed, compiled.seedBlock)
  );

  const installedContents = compiled.migrations.map((migration) => ({
    migration,
    content: fs.readFileSync(installedPath(migration), 'utf8'),
  }));
  const installedSeed = fs.readFileSync(paths.seedPath, 'utf8');
  const installedManifest = fs.readFileSync(paths.manifestPath, 'utf8');

  // A differing already-installed migration (v1, frozen) must be refused,
  // leaving every artifact — including the other, unrelated v2 file —
  // untouched. writeGeneratedArtifacts validates every migration before
  // writing any of them.
  const v1Installed = installedContents[0];
  fs.writeFileSync(installedPath(v1Installed.migration), '-- differing installed v1\n');
  assert.throws(
    () => generator.writeGeneratedArtifacts(compiled, paths),
    /new migration|refus|exists and differs/i,
    'normal generator writes must refuse to replace a differing installed migration'
  );
  assert.equal(
    fs.readFileSync(installedPath(v1Installed.migration), 'utf8'),
    '-- differing installed v1\n',
    'refusal must leave the installed migration untouched'
  );
  assert.equal(
    fs.readFileSync(paths.seedPath, 'utf8'),
    installedSeed,
    'migration refusal must leave the seed untouched'
  );
  assert.equal(
    fs.readFileSync(paths.manifestPath, 'utf8'),
    installedManifest,
    'migration refusal must leave the checksum manifest untouched'
  );
  const v2Installed = installedContents[1];
  assert.equal(
    fs.readFileSync(installedPath(v2Installed.migration), 'utf8'),
    v2Installed.content,
    'a refusal on one migration must not touch any other, unrelated migration file'
  );
  fs.writeFileSync(installedPath(v1Installed.migration), v1Installed.content);

  fs.rmSync(installedPath(v1Installed.migration));
  generator.writeGeneratedArtifacts(compiled, paths);
  assert.equal(
    fs.readFileSync(installedPath(v1Installed.migration), 'utf8'),
    v1Installed.content,
    'a missing migration may be restored when generated bytes match the recorded checksum'
  );
  assert.equal(
    fs.readFileSync(paths.seedPath, 'utf8'),
    installedSeed,
    'matching migration recovery must leave the seed untouched'
  );
  assert.equal(
    fs.readFileSync(paths.manifestPath, 'utf8'),
    installedManifest,
    'matching migration recovery must leave the checksum manifest untouched'
  );

  fs.rmSync(installedPath(v1Installed.migration));
  const seedBeforeRefusal = fs.readFileSync(paths.seedPath, 'utf8');
  const manifestBeforeRefusal = fs.readFileSync(paths.manifestPath, 'utf8');
  assert.throws(
    () => generator.writeGeneratedArtifacts(changedCompiled, paths),
    /different recorded checksum; restore it or create a new migration/i,
    'a missing migration must not be recreated from bytes that differ from its recorded checksum'
  );
  assert.equal(
    fs.existsSync(installedPath(v1Installed.migration)),
    false,
    'checksum refusal must leave the migration missing'
  );
  assert.equal(
    fs.readFileSync(paths.seedPath, 'utf8'),
    seedBeforeRefusal,
    'checksum refusal must leave the seed untouched'
  );
  assert.equal(
    fs.readFileSync(paths.manifestPath, 'utf8'),
    manifestBeforeRefusal,
    'checksum refusal must leave the manifest untouched'
  );
} finally {
  fs.rmSync(tmpDir, { recursive: true, force: true });
}

console.log('test-journal-catalog-generator: OK (versioned delta compiler, hash, markers, immutable write guard)');
